type MessageKey = keyof typeof messages.zh;

type I18nContextValue = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: MessageKey, params?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocale] = useState<Locale>("zh");
  const value = useMemo<I18nContextValue>(
    () => ({
      locale,
      setLocale,
      t: (key, params) => formatMessage(messages[locale][key], params)
    }),
    [locale]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return context;
}

function formatMessage(message: string, params?: Record<string, string | number>) {
  if (!params) return message;
  return Object.entries(params).reduce((text, [key, value]) => text.replaceAll(`{${key}}`, String(value)), message);
}

export function localizeRelativeTime(value: string, locale: Locale) {
  if (locale === "en") return value;
  return value
    .replace("min ago", "分钟前")
    .replace("h ago", "小时前")
    .replace("d ago", "天前");
}

export function localizeWorldSize(value: string, locale: Locale) {
  if (locale === "en") return value;
  return value.replace("Medium World", "中型世界").replace("Large World", "大型世界").replace("Small World", "小型世界");
}

export function localizeDifficulty(value: string, locale: Locale) {
  if (locale === "en") return value;
  const map: Record<string, string> = {
    Classic: "经典",
    Expert: "专家",
    Master: "大师"
  };
  return map[value] ?? value;
}
